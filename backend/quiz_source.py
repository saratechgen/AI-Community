def get_questions_for_stage(
    stage: int,
    n: int = 5,
    date_str: str | None = None,
    exclude_ids: set[str] | None = None,
) -> list[dict]:
    """
    Return n questions for a given stage for a specific date.

    Uses SHA-256 seeding: each (date_str, stage) pair produces a
    cryptographically independent seed — no sequential correlation.

    exclude_ids: question IDs used recently; excluded before shuffling.
    Falls back to full pool if exclusion leaves fewer than n questions.
    """
    if date_str is None:
        date_str = _date.today().isoformat()

    pool = [q for q in QUESTION_BANK if q["stage"] == stage]

    if exclude_ids:
        available = [q for q in pool if q["id"] not in exclude_ids]
        if len(available) >= n:
            pool = available

    seed = _daily_seed(date_str, stage)
    rng  = random.Random(seed)
    rng.shuffle(pool)
    return pool[:n]


