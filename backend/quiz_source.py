def _qid(text: str) -> str:
    return hashlib.md5(text.encode()).hexdigest()[:12]


def _daily_seed(date_str: str, stage: int) -> int:
    """
    SHA-256 of '{date_str}:stage:{stage}' → uniform 31-bit seed.
    Each (date, stage) pair is cryptographically independent —
    no sequential correlation between consecutive days or adjacent stages.
    """
    key = f"{date_str}:stage:{stage}".encode()
    return int(hashlib.sha256(key).hexdigest(), 16) % (2 ** 31)


